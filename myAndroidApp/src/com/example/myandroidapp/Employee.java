package com.example.myandroidapp;

public class Employee {
	
	String name;
	String statusMessage;
	String annotation;
	String time;
	String colorCode;
	
	public Employee(String name, String statusMsg, String annotation, String time, String color)
	{
		this.name = name;
		this.statusMessage = statusMsg;
		this.annotation = annotation;
		this.time = time;
		this.colorCode = color;
	}
	

}
